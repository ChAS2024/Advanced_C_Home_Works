/*
 * Задача 5-4-Двоичные нули
 * Для заданных натуральных чисел N и K требуется вычислить количество чисел от 1 до N, имеющих в двоичной записи ровно K нулей. 
 * Например, если N=8 и K=1, то мы можем записать все числа от 1 до 8 в двоичной системе счисления: 
 * 1, 10, 11, 100, 101, 110, 111 и 1000. Откуда видно, что только числа 10, 101 и 110 имеют ровно один ноль в записи, 
 * т.е. правильный ответ – 3.
 * Формат ввода:
 * На вход поступают два натуральных числа через пробел N и K, не превышающих 20001.
 * Нужно вывести одно целое число — количество чисел от 1 до N с K нулями в двоичном представлении.
*/
#include <stdio.h>

// Функция для подсчета количества нулей в двоичной записи числа
int count_zeros_in_binary(int num) {
    int zero_count = 0;
    while (num > 0) {
        if ((num & 1) == 0) {
            zero_count++;
        }
        num >>= 1;
    }
    return zero_count;
}

int main() {
    int N, K;
    int count = 0;

    // Ввод N и K
    scanf("%d %d", &N, &K);

    // Перебираем все числа от 1 до N
    for (int i = 1; i <= N; i++) {
        // Подсчитываем количество нулей в двоичном представлении числа
        if (count_zeros_in_binary(i) == K) {
            count++;
        }
    }

    // Выводим результат
    printf("%d\n", count);

    return 0;
}
